-- 4. 부서별 최고 급여(SAL) 금액을 아래 예제처럼 출력하세요?
--    ex) 메니저 없는 사원은 제외
SELECT 
	DEPTNO , max(SAL)
FROM 
	EMP e 
WHERE 
	MGR IS NOT NULL
GROUP BY 
	DEPTNO
ORDER BY 
	DEPTNO;


